using System.Collections.Generic;

namespace LyvinDeviceDriverLib
{
    public class DevicePolicy
    {
        public DevicePolicy(string id, string description, List<DevicePolicy> subPolicies, string type)
        {
            ID = id;
            Description = description;
            SubPolicies = new List<DevicePolicy>();
            foreach (var devicePolicy in subPolicies)
            {
                SubPolicies.Add(devicePolicy);
            }
            Type = type;
        }

        public string Description { get; set; }
        public string ID { get; set; }
        public List<DevicePolicy> SubPolicies { get; set; }
        public string Type { get; set; }
    }
}
