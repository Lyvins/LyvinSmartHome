using System.Collections.Generic;

namespace LyvinDeviceDriverLib.SystemComInterfaces
{
    public interface IComProtocol
    {

        string ID { get; set; }

        string FileName { get; set; }

        string ComType { get; set; }

        IComManager ComManager { get; set; }

        /// <summary>
        /// Settings and Data are now a generic which can be anything depending on the
        /// protocol implementing it.........
        /// </summary>
        /// <param name="settings"></param>
        /// <param name="data"></param>
        int Write(Dictionary<string, string> settings, string data, string pddID);

        int Listen(Dictionary<string, string> settings, string pddID);

        int StopListening(Dictionary<string, string> settings, string pddID);
    }
}