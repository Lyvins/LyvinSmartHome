namespace LyvinDeviceDriverLib
{
    /// <summary>
    ///        Return codes:
    ///          All PDD error codes start with 68 (ASCII D for Device)
    ///          680 - unexpected response
    ///          681 - unknown device
    ///          682 - illegal type
    ///          683 - unknown action
    ///          684 - illegal value
    ///          685 - illegal action
    ///          686 - invalid device settings
    ///          687 - Device is off
    ///          689 - custom driver error
    ///          0   - Success
    /// </summary>
    public enum ReturnCodes
    {
        Success = 0,
        UnexpectedResponse = 680,
        UnknownDevice = 681,
        IllegalType = 682,
        UnknownAction = 683,
        IllegalValue = 684,
        IllegalAction = 685,
        InvalidDeviceSettings = 686,
        DeviceOff = 687,
        CustomDriverError = 689
    };
}
