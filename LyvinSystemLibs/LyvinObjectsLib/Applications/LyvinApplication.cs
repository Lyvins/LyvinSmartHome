namespace LyvinObjectsLib.Applications
{
    public class LyvinApplication
    {

        public LyvinApplication()
        {

        }

        /// <summary>
        /// The constructor for creating an application.
        /// </summary>
        /// <param name="applicationID">The unique ID of the application</param>
        /// <param name="name">The name of the application</param>
        /// <param name="description">A description of the application</param>
        /// <param name="type">The type of the application</param>
        /// <param name="trusted">Indicator whether the application is trusted</param>
        public LyvinApplication(string applicationID, string name, string description, string type, bool trusted)
        {
            ApplicationID = applicationID;
            Name = name;
            Description = description;
            Type = type;
            Trusted = trusted;
        }

        public string ApplicationID { get; set; }

        public string Description { get; set; }

        public string Name { get; set; }

        public bool Trusted { get; set; }

        public string Type { get; set; }

    }
}