using System.ServiceModel;
using LyvinDeviceAPIContracts.DeviceAPIMessages;

namespace LyvinDeviceAPIContracts
{
    /// <summary>
    /// This service contract should be implemented by applications that want to
    /// receive events, the DeviceAPIApplication manager, widgets that want to receive events,
    /// the widget manager, and the Event Manager, to receive events from the system.
    /// </summary>
    [ServiceContract]
    public interface ISCDeviceEventAPI
    {

        /// 
        /// <param name="event"></param>
        [OperationContract(IsOneWay = true)]
        void DeviceChangedEvent(DeviceChangedEvent deviceEvent);

        /// 
        /// <param name="event"></param>
        [OperationContract(IsOneWay = true)]
        void DevicePreUpdateEvent(DevicePreUpdateEvent deviceEvent);
    }
}