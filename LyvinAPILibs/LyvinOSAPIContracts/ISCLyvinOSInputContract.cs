using System.ServiceModel;
using LyvinDeviceAPIContracts;
using LyvinElevationAPIContracts;

namespace LyvinOSAPIContracts
{
    /// <summary>
    /// Service Contract used specifically for the communication from the Event Manager to the LyvinOS.
    /// This has been made a seperate interface, inheriting the device and elevation interfaces, to be able
    /// to add functions specific to the EM->LyvinOS communication if necessary.
    /// </summary>
    [ServiceContract]
    public interface ISCLyvinOSInputContract : ISCDeviceRequestAPI, ISCElevationReplyAPI, ISCDeviceUpdateRequestAPI
    {
        [OperationContract]
        bool HandShake();

        [OperationContract]
        bool KeepAlive();
    }
}
